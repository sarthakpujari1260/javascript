// SWITCH CASE :


const month = 5

switch (month) {
    case 1:
        console.log("JAN");
        break;
    case 2:
        console.log("FEB");
        break;
    case 3:
        console.log("MARCH");
        break;
    case 4:
        console.log("APRIL");
        break;

    default:
        console.log("FAILED TO GET MONTH"); 
        break;
}